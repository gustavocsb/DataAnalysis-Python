'''
Pseudocódigo:
1. Declarar as variáveis (comprimento, altura e área)
2. Pedir ao user os valores de comprimento e altura
3. Calcular a área
4. Armazenar o resultado da área
5. Imprimir o valor da variável 'área'

'''

comprimento = float(input("Digite o valor do comprimento: "))
altura = float(input("Digite o valor da altura: "))

area = comprimento * altura

print("A área do paralelogramo é: ", area)