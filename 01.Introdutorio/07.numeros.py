# Soma
print(f"19 + 25 = {19+25}")

# Subtração
print(f"123 - 19 = {123-19}")

# Multiplicação
print(f"8123 * 3 = {8123*3}")

# Divisão
print(f"1293 / 129 = {1293/129}")

# Potência
print(f"4² = {4**2}")

# Módulo (Retorna o resto da divisão entre operandos)
print(f"10 % 3 = {10%3}")


# ----- Função 'type' verifica o tipo de dado -----
print(type(5))

print(type(5.0))

a = "Eu trabalho com Análise de Dados"
print(type(a))


# ----- Operações com números float -----

# retorna float
print(f"3.1 + 6.4 = {3.1+6.4}")

# retorna float
print(f"4 + 4.0 = {4+4.0}")

# retorna float
print(f"4 / 2 = {4/2}")

# retorna o número inteiro
print(f"4 // 2 = {4//2}")

# mudança nos outputs:
print(f"4 / 3.0 = {4/3.0}")
print(f"4 // 3.0 = {4//3.0}")


# ----- Conversão -----

print(float(9))
print(int(6.0))
print(int(6.5))
print(int(6.6))

# ----- Hexadecimal e Binário -----

print(hex(394))
print(hex(217))
