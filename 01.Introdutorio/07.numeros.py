# ----- Funções abs, round e pow -----

# retorna o valor absoluto
print(abs(-8))
print(abs(8))

# retorna o valor com duas casas decimais
print(round(3.14151922,2))

# Potência
print(f"4² = {pow(4,2)}")
print(f"5³ = {pow(5,3)}")